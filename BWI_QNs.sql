				ORDR_OPER_COMPLETED_DT, ORDR_STAT, ORDR_OCAT_CD
			)

		LEFT JOIN CDAS.TDWHNOTE ON QNOT_NO = NOTE_QNOT_NO AND QNDF_NO = NOTE_QNDF_NO 

